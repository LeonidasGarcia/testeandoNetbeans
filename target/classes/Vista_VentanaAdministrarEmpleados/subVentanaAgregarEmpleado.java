    private void agregarNuevoEmpleado() {

        String id = String.valueOf((int) Math.floor(Math.random() * 100000));

        Map<String, Object> datos = new HashMap<>();

        //AQUI DEBE ESTAR LA VALIDACION
        int errores = 0;
        errores += validacionTxtNormales(txtNombres);
        errores += validacionTxtNormales(txtApellidos);
        errores += validacionTxtNormales(txtCorreoElectronico);
        errores += validacionTxtNumeroCelular(txtNumeroCelular);
        errores += validacionSpinnerSueldo(spinnerSueldo);

        if (errores == 0) {
            //GUARDA LOS DATOS A ENVIAR
            datos.put("apellidos", txtApellidos.getText().trim());
            datos.put("nombres", txtNombres.getText().trim());
            datos.put("cargo", cbCargo.getSelectedItem().toString().trim());
            datos.put("sueldo", spinnerSueldo.getValue().toString().trim());
            datos.put("numero_celular", txtNumeroCelular.getText().trim());
            datos.put("correo_electronico", txtCorreoElectronico.getText().trim() + cbTipoCorreo.getSelectedItem().toString().trim());

            //ENVIA LOS DATOS
            ConectorFireBase.guardarEmpleado("Empleados", id, datos);

            JOptionPane.showMessageDialog(null, "Empleado agregado correctamente");

            reiniciarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Uno o mas datos ingresados son incorrectos, revíselo y reintente nuevamente");
            reiniciarFondos();
        }

    }

    private int validacionTxtNormales(JTextField txt) {

        if ("".equals(txt.getText())) {
            txt.setBackground(Color.red);
            return 1;
        } else {
            return 0;
        }

    }

    private int validacionTxtNumeroCelular(JTextField txt) {
        if (txt.getText().length() != 9) {
            txt.setBackground(Color.red);
            return 1;
        } else if (txt.getText().startsWith("9")) {
            return 0;
        } else {
            txt.setBackground(Color.red);
            return 1;
        }
    }

    private int validacionSpinnerSueldo(JSpinner spinner) {
        if ((int) spinner.getValue() == 0) {
            return 1;
        } else {
            return 0;
        }
    }

    private void reiniciarCampos() {
        txtApellidos.setText("");
        txtNombres.setText("");
        spinnerSueldo.setValue(0);
        txtNumeroCelular.setText("");
        txtCorreoElectronico.setText("");
        cbTipoCorreo.setSelectedIndex(0);
        cbCargo.setSelectedIndex(0);
    }

    private void reiniciarFondos () {
        txtNombres.setBackground(Color.white);
        txtApellidos.setBackground(Color.white);
        txtNumeroCelular.setBackground(Color.white);
        txtCorreoElectronico.setBackground(Color.white);
    }
    
